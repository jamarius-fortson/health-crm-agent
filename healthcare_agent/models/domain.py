class MessageDirection(str, Enum):
    INBOUND = "inbound"
    OUTBOUND = "outbound"


class MessageChannel(str, Enum):
    SMS = "sms"
    EMAIL = "email"
    VOICE = "voice"
    PORTAL = "portal"


class MessageStatus(str, Enum):
    PENDING = "pending"
    SENT = "sent"
    DELIVERED = "delivered"
    FAILED = "failed"
    BLOCKED = "blocked"  # Blocked by content classifier
    ESCALATED = "escalated"  # Escalated to HITL


class Message(BaseModel):
    """
    Represents a patient communication message.

    Every outbound message passes through the clinical content classifier.
    Messages flagged as clinical are blocked and escalated to HITL.
    """
    model_config = ConfigDict(extra="forbid")

    id: str = Field(default_factory=lambda: str(uuid.uuid4()))

    patient_id: str = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    direction: MessageDirection = Field(
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    channel: MessageChannel = Field(
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    to_address: str = Field(
        max_length=255,
        json_schema_extra={"phi_type": PHIType.PHI},
        description="Phone number or email — PHI because it identifies the patient",
    )
    body: str = Field(
        max_length=5000,
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    status: MessageStatus = Field(
        default=MessageStatus.PENDING,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # Content classification results
    clinical_content_detected: bool = Field(
        default=False,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
        description="Set by clinical content classifier",
    )
    classifier_confidence: float | None = Field(
        default=None,
        ge=0.0,
        le=1.0,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    classification_categories: list[str] = Field(
        default_factory=list,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # HITL
    requires_hitl: bool = Field(
        default=False,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    hitl_reason: str | None = Field(
        default=None,
        max_length=500,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    sent_at: datetime | None = Field(default=None)
    created_at: datetime = Field(default_factory=datetime.utcnow)

    # Campaign linkage (for recall campaigns)
    campaign_id: str | None = Field(
        default=None,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )


