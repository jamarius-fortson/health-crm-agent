class PriorAuthorization(BaseModel):
    """
    Prior authorization record.

    CRITICAL: The clinical_justification field is CLINICAL and must be
    authored by a clinician. The system NEVER generates this field autonomously.
    """
    model_config = ConfigDict(extra="forbid")

    id: str = Field(default_factory=lambda: str(uuid.uuid4()))

    patient_id: str = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    insurance_plan_id: str = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    # Procedure info (OPERATIONAL — codes and templates, not clinical reasoning)
    procedure_code: str = Field(
        max_length=10,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
        description="CPT/HCPCS procedure code",
    )
    procedure_description: str = Field(
        max_length=500,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # CLINICAL — clinician-authored only, never system-generated
    clinical_justification: str | None = Field(
        default=None,
        max_length=5000,
        json_schema_extra={"phi_type": PHIType.CLINICAL},
        description="MUST be authored by a clinician. Never system-generated.",
    )
    diagnosing_clinician_id: str | None = Field(
        default=None,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.CLINICAL},
    )

    # Status
    status: PriorAuthStatus = Field(
        default=PriorAuthStatus.DRAFT,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    submitted_at: datetime | None = Field(default=None)
    approved_at: datetime | None = Field(default=None)
    expiration_date: date | None = Field(default=None)

    # Response
    prior_auth_number: str | None = Field(
        default=None,
        max_length=50,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    denial_reason: str | None = Field(
        default=None,
        max_length=1000,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    created_at: datetime = Field(default_factory=datetime.utcnow)
    updated_at: datetime = Field(default_factory=datetime.utcnow)


