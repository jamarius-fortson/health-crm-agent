"""
Core domain models for the healthcare CRM agent.

Every model carries explicit PHI field annotations so the compliance layer
can enforce minimum-necessary access at runtime. Fields are tagged with:
  - PHI: contains protected health information (18 HIPAA identifiers)
  - OPERATIONAL: non-PHI operational data
  - CLINICAL: clinical data (never generated by autonomous agents)

Models use Pydantic v2 with strict validation.
"""

from __future__ import annotations

import uuid
from datetime import datetime, date
from enum import Enum
from typing import Any

from pydantic import BaseModel, Field, field_validator, ConfigDict


# ============================================================================
# PHI Field Classification Enum
# ============================================================================

class PHIType(str, Enum):
    """Classification of every field for HIPAA compliance."""
    PHI = "phi"              # Protected health information
    OPERATIONAL = "operational"  # Non-PHI operational data
    CLINICAL = "clinical"    # Clinical data (requires clinician access only)


# ============================================================================
# Enums
# ============================================================================

class PatientStatus(str, Enum):
    ACTIVE = "active"
    INACTIVE = "inactive"
    DECEASED = "deceased"
    ARCHIVED = "archived"


class AppointmentType(str, Enum):
    """Clinic-defined appointment types. Never freeform clinical description."""
    NEW_PATIENT = "new_patient"
    FOLLOW_UP = "follow_up"
    ANNUAL_PHYSICAL = "annual_physical"
    SICK_VISIT = "sick_visit"
    PROCEDURE = "procedure"
    TELEHEALTH = "telehealth"
    CONSULTATION = "consultation"


class AppointmentStatus(str, Enum):
    SCHEDULED = "scheduled"
    CONFIRMED = "confirmed"
    CHECKED_IN = "checked_in"
    COMPLETED = "completed"
    CANCELLED = "cancelled"
    NO_SHOW = "no_show"


# ============================================================================
# Patient Model
# ============================================================================

class Patient(BaseModel):
    """
    Represents a patient in the practice.

    PHI fields are explicitly annotated so the PHI scope enforcement
    layer can strip fields at runtime based on agent permissions.
    """
    model_config = ConfigDict(extra="forbid")

    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    fhir_resource_id: str | None = Field(
        default=None,
        description="FHIR Patient resource ID if synced with EHR",
    )

    # --- Demographics (PHI) ---
    first_name: str = Field(
        min_length=1,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    last_name: str = Field(
        min_length=1,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    date_of_birth: date = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    sex: str | None = Field(
        default=None,
        max_length=20,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    gender_identity: str | None = Field(
        default=None,
        max_length=50,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    race: str | None = Field(
        default=None,
        max_length=50,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    ethnicity: str | None = Field(
        default=None,
        max_length=50,
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    # --- Contact (PHI) ---
    phone: str | None = Field(
        default=None,
        max_length=20,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    email: str | None = Field(
        default=None,
        max_length=255,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    preferred_contact_method: str = Field(
        default="phone",
        max_length=20,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # --- Address (PHI) ---
    street_address: str | None = Field(
        default=None,
        max_length=255,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    city: str | None = Field(
        default=None,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    state: str | None = Field(
        default=None,
        max_length=2,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    zip_code: str | None = Field(
        default=None,
        max_length=10,
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    # --- Emergency Contact (PHI) ---
    emergency_contact_name: str | None = Field(
        default=None,
        max_length=200,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    emergency_contact_phone: str | None = Field(
        default=None,
        max_length=20,
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    emergency_contact_relationship: str | None = Field(
        default=None,
        max_length=100,
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    # --- Insurance (PHI) ---
    primary_insurance_plan_id: str | None = Field(
        default=None,
        json_schema_extra={"phi_type": PHIType.PHI},
    )

    # --- Preferences (OPERATIONAL) ---
    language: str = Field(
        default="en",
        max_length=10,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    communication_preferences: dict[str, Any] = Field(
        default_factory=dict,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # --- Metadata ---
    created_at: datetime = Field(default_factory=datetime.utcnow)
    updated_at: datetime = Field(default_factory=datetime.utcnow)
    status: PatientStatus = Field(default=PatientStatus.ACTIVE)

    @field_validator("state")
    @classmethod
    def validate_state_code(cls, v: str | None) -> str | None:
        if v is not None and len(v) != 2:
            raise ValueError("State must be a 2-letter US state code")
        return v


