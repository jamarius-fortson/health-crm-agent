class Appointment(BaseModel):
    """
    Represents a scheduled appointment.

    Only the appointment type and time are stored — never freeform
    clinical reasoning about what the appointment is for.
    """
    model_config = ConfigDict(extra="forbid")

    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    fhir_resource_id: str | None = Field(default=None)

    patient_id: str = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    provider_id: str = Field(
        max_length=100,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    appointment_type: AppointmentType = Field(
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    appointment_type_display: str = Field(
        max_length=100,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
        description="Human-readable display name from clinic configuration",
    )

    start_time: datetime = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    end_time: datetime = Field(
        json_schema_extra={"phi_type": PHIType.PHI},
    )
    timezone: str = Field(
        default="America/New_York",
        max_length=50,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    location: str | None = Field(
        default=None,
        max_length=200,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    visit_type: str = Field(
        default="in_person",
        max_length=20,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    status: AppointmentStatus = Field(
        default=AppointmentStatus.SCHEDULED,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # Scheduling metadata
    created_by: str = Field(
        default="system",
        max_length=100,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    created_at: datetime = Field(default_factory=datetime.utcnow)
    updated_at: datetime = Field(default_factory=datetime.utcnow)

    # No-show prediction
    no_show_risk: float | None = Field(
        default=None,
        ge=0.0,
        le=1.0,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
        description="Predicted no-show probability (0.0–1.0)",
    )

    # Flags requiring HITL
    requires_hitl: bool = Field(
        default=False,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )
    hitl_reason: str | None = Field(
        default=None,
        max_length=500,
        json_schema_extra={"phi_type": PHIType.OPERATIONAL},
    )

    # Patient linkage (PHI — links patient to this appointment)
    patient_name: str | None = Field(
        default=None,
        max_length=200,
        json_schema_extra={"phi_type": PHIType.PHI},
        description="Included only for agents with patient-name scope",
    )


